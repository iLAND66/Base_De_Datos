#primera letra del nombre + apellido + primera letra del seg apellido
#funcion para crear correos
crear_correo <- function(nombre_completo) {
  partes <- strsplit(nombre_completo, " ")[[1]]
  primer_nombre <- partes[1]
  primer_apellido <- partes[length(partes)-1]
  segundo_apellido <- partes[length(partes)]
  correo <- paste0(substr(primer_nombre, 1, 1),
                   primer_apellido,
                   substr(segundo_apellido, 1, 1),
                   "@ipn.mx")
  correo <- tolower(correo)
  return(correo)
}

